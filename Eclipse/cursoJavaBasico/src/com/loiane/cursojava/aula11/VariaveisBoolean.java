package com.loiane.cursojava.aula11;

public class VariaveisBoolean {

	public static void main(String[] args) {
		
		boolean verdadeiro = true;
		boolean falso = false;

		System.out.println("O valor de verdadeiro é " + verdadeiro);
		System.out.println("O valor de falso é " + falso);
		
		//testando if e else conditions
		if (verdadeiro == false) {
			
			System.out.println("if foi executado");
		} else {
			
			System.out.println("else foi executado");
		}
		
	}

}
